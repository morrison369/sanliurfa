def process_astro_file(filepath):
    with open(filepath, 'r', encoding='utf-8') as f:
        content = f.read()
    
    # Find lucide-react import
    match = re.search(r"import\s*\{([^}]+)\}\s*from\s*['\"]lucide-react['\"]", content)
    if not match:
        return False
    
    imported_icons = [name.strip() for name in match.group(1).split(',')]
    
    # Build icons object
    icons_obj = "const icons = {\n"
    for icon in imported_icons:
        if icon in ICONS:
            safe_name = re.sub(r'[^a-zA-Z0-9_]', '', icon)
            icons_obj += f"  {safe_name}: `{ICONS[icon]}`,\n"
    icons_obj += "};\n\n"
    
    # Replace import with icons object
    content = content.replace(match.group(0), icons_obj)
    
    # Replace component usages
    for icon in imported_icons:
        safe_name = re.sub(r'[^a-zA-Z0-9_]', '', icon)
        # Replace <IconName className="..." /> with <span set:html={icons.safe_name}></span>
        pattern = rf'<{re.escape(icon)}\s+className="([^"]*)"\s*/>'
        def replace_icon(m):
            cls = m.group(1)
            svg = ICONS.get(icon, '')
            # Update class in SVG
            svg = svg.replace('class="w-5 h-5"', f'class="{cls}"')
            return f'<span set:html={{icons.{safe_name}}}></span>'
        content = re.sub(pattern, replace_icon, content)
    
    with open(filepath, 'w', encoding='utf-8') as f:
        f.write(content)
    
    print(f'  Fixed: {filepath}')
    return True

# Find all .astro files with lucide-react
astro_files = glob.glob('src/components/**/*.astro', recursive=True) + \
              glob.glob('src/pages/**/*.astro', recursive=True)

fixed = 0
for f in astro_files:
    try:
        with open(f, 'r', encoding='utf-8') as file:
            if 'lucide-react' in file.read():
                if process_astro_file(f):
                    fixed += 1
    except:
        pass

print(f'\nFixed {fixed} files!')
