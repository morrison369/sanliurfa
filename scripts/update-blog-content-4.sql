  content = $$<h2>Şanlıurfa Çiğ Köftesinin Hikayesi</h2>
<p>Çiğ köfte, adından da anlaşılacağı üzere "çiğ et" ile yapılan bir yemekti; ancak günümüzde gıda yönetmelikleri nedeniyle restoran ve marketlerde satılan çiğ köfte tamamen bitkisel malzemelerden üretilmektedir. Şanlıurfa'nın otantik çiğ köftesi ise hâlâ ince çekilmiş bulgur, yoğun baharat ve emek isteyen bir yoğurma tekniğiyle hazırlanır.</p>
<p>Efsaneye göre Hz. İbrahim, Nemrut tarafından yakarılmak üzere odun toplamaya gönderilir; Nemrut'un askerleri ateş yakmak için bütün odunları toplayıp tüketir. Bu nedenle hiç ateş olmadan yemek pişirmek zorunda kalan köylüler, eti baharatla yoğurarak çiğ köfteyi icat etmiştir. Efsane şehirdir; lezzeti ise gerçektir.</p>

<h2>Geleneksel Çiğ Köfte Malzemeleri</h2>
<p>4 kişilik porsiyon için:</p>
<ul>
  <li>500 g ince bulgur (köftelik, en ince çekim)</li>
  <li>250 g çok ince çekilmiş kuzu kıyması (geleneksel; bitkisel versiyonda çıkarılır)</li>
  <li>2 yemek kaşığı domates salçası</li>
  <li>1 yemek kaşığı biber salçası</li>
  <li>2 çay kaşığı isot biberi (Şanlıurfa'ya özgü kurutulmuş kırmızı biber)</li>
  <li>1 çay kaşığı karabiber</li>
  <li>1 çay kaşığı kimyon</li>
  <li>1 çay kaşığı kişniş</li>
  <li>Tuz (tatlandırmak için)</li>
  <li>Yarım demet taze maydanoz (ince kıyılmış)</li>
  <li>2 orta boy soğan (rendelenmiş)</li>
  <li>2 yemek kaşığı nar ekşisi</li>
  <li>1 limonun suyu</li>
</ul>

<h2>Adım Adım Yapılışı</h2>
<ol>
  <li><strong>Bulguru ıslatın:</strong> İnce bulguru geniş bir kaba alın. Üzerine ılık su ekleyip 20–30 dakika bekletin; bulgur şişecektir. Fazla suyu süzün.</li>
  <li><strong>Salçaları karıştırın:</strong> Domates ve biber salçasını bulgura ekleyin. İyi karıştırın; salçanın bulgura eşit dağılmasını sağlayın.</li>
  <li><strong>Baharatları ekleyin:</strong> İsot, karabiber, kimyon, kişniş ve tuzu ekleyin.</li>
  <li><strong>Yoğurmaya başlayın:</strong> Bu adım en kritik kısımdır. Karışımı avuçlarınızla bastırarak, sıkarak, döndürerek en az 20–30 dakika yoğurun. Yoğurma süresi kısaltılırsa bulgur tam lezzetini vermiyor. Geleneksel çiğ köftede bu yoğurma sabahtan akşama kadar sürebilir.</li>
  <li><strong>Soğan ve nar ekşisi:</strong> Rendelenmiş soğan suyunu (posası değil sadece suyu) ve nar ekşisini ekleyip yoğurmaya devam edin.</li>
  <li><strong>Son dokunuş:</strong> İnce kıyılmış taze maydanozu ve limon suyunu ekleyip karıştırın. Tadına bakın; gerekirse tuz veya limon ilave edin.</li>
  <li><strong>Dinlendirin:</strong> Buzdolabında 1 saat dinlendirin; bulgur baharatları içine iyice çeker.</li>
</ol>

<h2>Servis Önerileri</h2>
<ul>
  <li>Marul yaprakları üzerine küçük toplar halinde servis edilir.</li>
  <li>Üzerine limon sıkılır, isot biberi serpilir.</li>
  <li>Yanında taze soğan, domates ve salatalık eşliğinde sunulur.</li>
  <li>Geleneksel olarak lavash ekmeğine sarılarak yenilir.</li>
</ul>

<h2>İsot Biberi Nerede Bulunur?</h2>
<p>İsot biberi Şanlıurfa'ya özgüdür. Şehrin Kapalıçarşısı'nda ve Bakırcılar Çarşısı yakınındaki baharat dükkânlarında taze ve vakumlu pakette satılmaktadır. İstanbul ve Ankara'daki Güneydoğu mutfağına odaklanan aktarlarda da bulunabilir. Online alışveriş platformlarından da sipariş verilebilir.</p>

<h2>Sık Sorulan Sorular</h2>
<dl>
  <dt><strong>Şanlıurfa çiğ köftesi ile market çiğ köftesi arasındaki fark nedir?</strong></dt>
  <dd>Market ve zincir çiğ köftecilerdeki ürünler tamamen bitkisel, hazır karışımlı ve lezzet profili standartlaştırılmıştır. Şanlıurfa'nın geleneksel çiğ köftesi ise taze baharatlar, isot biberi ve uzun yoğurma süresiyle çok daha derin ve karmaşık bir aromaya sahiptir.</dd>
  <dt><strong>Çiğ köfte acılı mı? Acısız versiyonu var mı?</strong></dt>
  <dd>Geleneksel Şanlıurfa çiğ köftesi orta-yüksek acılıdır; isot biberi keskin bir acılık katar. Evde yapımda isot miktarı azaltılarak hafif acılı ya da acısız yapılabilir. Restoranlarda da talebe göre ayarlama yapılabilir.</dd>
  <dt><strong>Çiğ köfte buzdolabında kaç gün saklanır?</strong></dt>
  <dd>Taze yapılan çiğ köfte buzdolabında 2–3 gün tazeliğini korur. Marul yapraklarına sarılmış halde saklamayın; ekmeği veya marul sarmasını servis anında yapın. Uzun saklamak için buzdolabında hava almaz kapta muhafaza edin.</dd>
</dl>
